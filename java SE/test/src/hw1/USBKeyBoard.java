package hw1;

public class USBKeyBoard extends KeyBoard {
    public USBKeyBoard() {
        super();
    }

    public USBKeyBoard(String name) {
        super(name);
    }

    @Override
    public String getName() {
        return super.getName();
    }

    @Override
    public void setName(String name) {
        super.setName(name);
    }

    @Override
    public void install() {
        super.install();
    }

    @Override
    public void uninstall() {
        super.uninstall();
    }
}
