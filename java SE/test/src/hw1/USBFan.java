package hw1;

public class USBFan extends Fan {
    public USBFan() {
        super();
    }

    public USBFan(String name) {
        super(name);
    }

    @Override
    public String getName() {
        return super.getName();
    }

    @Override
    public void setName(String name) {
        super.setName(name);
    }

    @Override
    public void install() {
        super.install();
    }

    @Override
    public void uninstall() {
        super.uninstall();
    }
}
